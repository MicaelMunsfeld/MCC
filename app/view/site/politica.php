    <h1>Política de Segurança da Informação</h1>
    <p>A Sálvio Automóveis compromete-se a garantir a segurança das informações de todos os seus clientes, parceiros, fornecedores e colaboradores. Esta Política de Segurança da Informação define as diretrizes e práticas para proteger a confidencialidade, integridade e disponibilidade das informações dentro da organização.</p>

    <h2>1. Introdução</h2>
    <p>A Sálvio Automóveis compromete-se a garantir a segurança das informações de todos os seus clientes, parceiros, fornecedores e colaboradores. Esta Política de Segurança da Informação define as diretrizes e práticas para proteger a confidencialidade, integridade e disponibilidade das informações dentro da organização.</p>

    <h2>2. Objetivo</h2>
    <p>Estabelecer um conjunto de diretrizes que assegurem a proteção das informações contra ameaças, sejam elas internas ou externas, intencionais ou acidentais.</p>

    <h2>3. Escopo</h2>
    <p>Esta política aplica-se a todos os colaboradores, parceiros, fornecedores e qualquer outra entidade que tenha acesso às informações da Sálvio Automóveis.</p>

    <h2>4. Princípios</h2>
    <ul>
        <li><strong>Confidencialidade:</strong> Assegurar que as informações sejam acessíveis apenas às pessoas autorizadas.</li>
        <li><strong>Integridade:</strong> Garantir a precisão e integridade das informações e dos métodos de processamento.</li>
        <li><strong>Disponibilidade:</strong> Assegurar que as informações e os serviços estejam disponíveis para os usuários autorizados sempre que necessário.</li>
    </ul>

    <h2>5. Diretrizes Gerais</h2>
    <h3>Controle de Acesso</h3>
    <p>O acesso às informações deve ser restrito a usuários autorizados. Implementar autenticação robusta para acessar sistemas e dados. Revisar e atualizar regularmente as permissões de acesso.</p>

    <h3>Proteção de Dados Pessoais</h3>
    <p>Coletar, armazenar e tratar dados pessoais de acordo com a LGPD (Lei Geral de Proteção de Dados - Lei nº 13.709/18). Implementar medidas de segurança para proteger dados pessoais contra acesso não autorizado, alteração, divulgação ou destruição.</p>

    <h3>Classificação da Informação</h3>
    <p>Classificar as informações com base em sua sensibilidade e criticidade. Aplicar controles apropriados de acordo com a classificação da informação.</p>

    <h3>Gestão de Riscos</h3>
    <p>Identificar e avaliar regularmente os riscos de segurança da informação. Implementar controles para mitigar os riscos identificados.</p>

    <h3>Educação e Conscientização</h3>
    <p>Realizar treinamentos regulares sobre segurança da informação para todos os colaboradores. Promover a conscientização sobre a importância da segurança da informação.</p>

    <h3>Gestão de Incidentes de Segurança</h3>
    <p>Estabelecer procedimentos para detectar, responder e recuperar-se de incidentes de segurança da informação. Manter registros de todos os incidentes e ações tomadas para resolvê-los.</p>

    <h3>Proteção de Infraestrutura</h3>
    <p>Implementar medidas de segurança física e lógica para proteger os sistemas de informação. Realizar manutenção regular e atualizações de software para garantir a segurança dos sistemas.</p>

    <h3>Uso de Recursos de TI</h3>
    <p>Estabelecer políticas de uso aceitável de recursos de TI. Monitorar o uso dos recursos para detectar e prevenir atividades inadequadas ou não autorizadas.</p>

    <h3>Proteção de Dados de Veículos</h3>
    <p>Garantir que as informações dos veículos, incluindo históricos de manutenção, dados de performance e informações de localização, sejam protegidas contra acessos não autorizados.</p>

    <h3>Segurança em Test Drives e Veículos de Frota</h3>
    <p>Proteger os sistemas de telemetria e rastreamento dos veículos de frota contra acessos não autorizados. Estabelecer procedimentos de segurança para o uso de veículos em test drives e operações de frota.</p>

    <h2>6. Responsabilidades</h2>
    <h3>Diretoria</h3>
    <p>Aprovar e revisar a Política de Segurança da Informação. Assegurar recursos adequados para a implementação da política.</p>

    <h3>Gestores</h3>
    <p>Implementar e monitorar os controles de segurança em suas áreas de responsabilidade. Reportar quaisquer incidentes de segurança à equipe de TI.</p>

    <h3>Colaboradores</h3>
    <p>Cumprir com a Política de Segurança da Informação. Reportar imediatamente qualquer incidente ou suspeita de incidente de segurança.</p>

    <h3>Equipe de TI</h3>
    <p>Implementar e manter controles técnicos de segurança. Monitorar e responder a incidentes de segurança da informação.</p>

    <h2>7. Revisão e Atualização</h2>
    <p>Esta Política de Segurança da Informação deve ser revisada e atualizada anualmente ou sempre que houver mudanças significativas nos sistemas ou nos requisitos legais e regulatórios.</p>

    <h2>8. Contato</h2>
    <p>Para dúvidas ou mais informações sobre esta política, entre em contato com a equipe de TI da Sálvio Automóveis através do e-mail: contato@sálvioautomoveis.com</p>
